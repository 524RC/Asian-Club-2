function clearName() {
    if (document.getElementById('nameField').value == 'Give a suggestion') {
        document.getElementById('nameField').value = '';
    }
}
function resetName() {
    if (document.getElementById('nameField').value == '') {
        document.getElementById('nameField').value = 'Give a suggestion';
    }
}
